#!/usr/bin/env node
// Regenerate CATALOG.md + index.md from scripts/catalog.json.
// This is a structured wiki: a "roadmap" is a suggested path, and each node can
// have a lesson under roadmaps/<catDir>/<slug>/lessons/<group>/<node>.md.
// Coverage = lessons written / nodes in the path. Do not hand-edit CATALOG.md.
import { readFileSync, writeFileSync, existsSync, readdirSync, statSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const ROOT = dirname(dirname(fileURLToPath(import.meta.url)));
const catalog = JSON.parse(readFileSync(join(ROOT, "scripts", "catalog.json"), "utf8"));

const CATEGORY_ORDER = ["Role-based", "Skill-based", "Best Practices", "Beginner"];
const CAT_DIR = {
  "Role-based": "role-based",
  "Skill-based": "skill-based",
  "Best Practices": "best-practices",
  Beginner: "beginner",
};
const BAR_WIDTH = 10;

const roadmapUrl = (r) =>
  r.category === "Best Practices"
    ? `https://roadmap.sh/best-practices/${r.slug}`
    : `https://roadmap.sh/${r.slug}`;

// Count node bullets inside the "## Nodes" section (top-level "- " lines only).
function countNodes(md) {
  const m = md.match(/\n## Nodes\n([\s\S]*?)(?=\n## |\s*$)/);
  if (!m) return 0;
  return (m[1].match(/^- \S/gm) || []).length;
}

// Count lesson files under lessons/**, excluding the index.md MOCs.
function countLessons(dir) {
  const lessons = join(dir, "lessons");
  if (!existsSync(lessons)) return 0;
  let n = 0;
  const walk = (d) => {
    for (const e of readdirSync(d, { withFileTypes: true })) {
      const p = join(d, e.name);
      if (e.isDirectory()) walk(p);
      else if (e.name.endsWith(".md") && e.name !== "index.md") n++;
    }
  };
  walk(lessons);
  return n;
}

function bar(pct) {
  const filled = Math.round((pct / 100) * BAR_WIDTH);
  return "▓".repeat(filled) + "░".repeat(BAR_WIDTH - filled);
}

function inspect(r) {
  const dir = join(ROOT, "roadmaps", CAT_DIR[r.category], r.slug);
  const readme = join(dir, "README.md");
  if (!existsSync(readme)) {
    return { path: null, nodes: 0, lessons: 0, pct: 0, touched: "—" };
  }
  const md = readFileSync(readme, "utf8");
  const nodes = countNodes(md);
  const lessons = countLessons(dir);
  const pct = nodes ? Math.round((lessons / nodes) * 100) : 0;
  const touched = statSync(readme).mtime.toISOString().slice(0, 10);
  return { path: `roadmaps/${CAT_DIR[r.category]}/${r.slug}/`, nodes, lessons, pct, touched };
}

const rows = catalog.roadmaps.map((r) => ({ ...r, ...inspect(r) }));
const totalLessons = rows.reduce((a, r) => a + r.lessons, 0);
const totalNodes = rows.reduce((a, r) => a + r.nodes, 0);
const withLessons = rows.filter((r) => r.lessons > 0).length;

let out = `# Catalog\n\n`;
out += `> Generated by \`node scripts/build-dashboard.mjs\` — do not hand-edit.\n\n`;
out += `**${rows.length}** roadmaps · **${totalLessons}** lessons written across **${withLessons}** track(s) · ${totalNodes} nodes mapped.\n\n`;
out += `A *roadmap* is a suggested path; each node can link to a *lesson*. Coverage = lessons written / nodes. `;
out += `⭐ = suggested starting point. See [CONVENTIONS.md](./CONVENTIONS.md) for how the wiki is structured.\n\n`;

for (const cat of CATEGORY_ORDER) {
  const group = rows.filter((r) => r.category === cat);
  if (!group.length) continue;
  group.sort(
    (a, b) =>
      Number(b.priority) - Number(a.priority) ||
      Number(b.lessons > 0) - Number(a.lessons > 0) ||
      a.title.localeCompare(b.title)
  );
  out += `## ${cat}\n\n`;
  out += `| | Roadmap | Coverage | Updated |\n`;
  out += `|---|---|---|---|\n`;
  for (const r of group) {
    const star = r.priority ? "⭐" : "";
    const name = r.path
      ? `[${r.title}](${r.path}) · [↗](${roadmapUrl(r)})`
      : `[${r.title}](${roadmapUrl(r)})`;
    const cov = r.path ? `\`${bar(r.pct)}\` ${r.lessons}/${r.nodes}` : "—";
    out += `| ${star} | ${name} | ${cov} | ${r.touched} |\n`;
  }
  out += `\n`;
}

writeFileSync(join(ROOT, "CATALOG.md"), out.replace(/\n+$/, "\n"));

// index.md = the Quartz site home page (same table, titled for the site).
const home = `---\ntitle: tech-studies\n---\n${out.replace(/^# Catalog\n/, "")}`;
writeFileSync(join(ROOT, "index.md"), home.replace(/\n+$/, "\n"));

console.log(
  `CATALOG.md + index.md written — ${rows.length} roadmaps, ${totalLessons} lessons, ${totalNodes} nodes.`
);
