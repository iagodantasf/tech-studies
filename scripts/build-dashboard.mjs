#!/usr/bin/env node
// Regenerate DASHBOARD.md from scripts/catalog.json + the checkboxes in each
// roadmaps/<slug>/README.md. Do not hand-edit DASHBOARD.md.
import { readFileSync, writeFileSync, existsSync, statSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const ROOT = dirname(dirname(fileURLToPath(import.meta.url)));
const catalog = JSON.parse(readFileSync(join(ROOT, "scripts", "catalog.json"), "utf8"));

const CATEGORY_ORDER = ["Role-based", "Skill-based", "Beginner", "Best Practices"];
const BAR_WIDTH = 10;
const EMOJI = { "not-started": "⚪", learning: "🟡", done: "🟢" };

const roadmapUrl = (r) =>
  r.category === "Best Practices"
    ? `https://roadmap.sh/best-practices/${r.slug}`
    : `https://roadmap.sh/${r.slug}`;

function parseStatus(md) {
  const fm = md.match(/^---\n([\s\S]*?)\n---/);
  if (!fm) return null;
  const m = fm[1].match(/^status:\s*([\w-]+)/m);
  return m ? m[1] : null;
}

function countCheckboxes(md) {
  const matches = md.match(/^[ \t]*- \[( |x|X)\]/gm) || [];
  const total = matches.length;
  const done = matches.filter((s) => /\[(x|X)\]/.test(s)).length;
  return { total, done };
}

function bar(pct) {
  const filled = Math.round((pct / 100) * BAR_WIDTH);
  return "▓".repeat(filled) + "░".repeat(BAR_WIDTH - filled);
}

function inspect(r) {
  const folder = join(ROOT, "roadmaps", r.slug);
  const readme = join(folder, "README.md");
  if (!existsSync(readme)) {
    return { status: "not-started", pct: 0, total: 0, done: 0, touched: "—", hasFolder: false };
  }
  const md = readFileSync(readme, "utf8");
  const { total, done } = countCheckboxes(md);
  const pct = total ? Math.round((done / total) * 100) : 0;
  let status = parseStatus(md);
  if (!status) status = total && pct === 100 ? "done" : "learning";
  const touched = statSync(readme).mtime.toISOString().slice(0, 10);
  return { status, pct, total, done, touched, hasFolder: true };
}

const rows = catalog.roadmaps.map((r) => ({ ...r, ...inspect(r) }));

// ── summary ──
const started = rows.filter((r) => r.hasFolder).length;
const done = rows.filter((r) => r.status === "done").length;
const learning = rows.filter((r) => r.status === "learning").length;

let out = `# Dashboard\n\n`;
out += `> Generated by \`node scripts/build-dashboard.mjs\` — do not hand-edit.\n\n`;
out += `**${rows.length}** roadmaps · 🟢 **${done}** done · 🟡 **${learning}** in progress · ⚪ **${rows.length - started}** not started\n\n`;
out += `⭐ = suggested starter (CS/foundations · AI/research · backend/infra · languages). See [CONVENTIONS.md](./CONVENTIONS.md) for the learning loop.\n\n`;

for (const cat of CATEGORY_ORDER) {
  const group = rows.filter((r) => r.category === cat);
  if (!group.length) continue;
  // priority first, then started, then alpha
  group.sort(
    (a, b) =>
      Number(b.priority) - Number(a.priority) ||
      Number(b.hasFolder) - Number(a.hasFolder) ||
      a.title.localeCompare(b.title)
  );
  out += `## ${cat}\n\n`;
  out += `| | Roadmap | Status | Progress | Updated |\n`;
  out += `|---|---|---|---|---|\n`;
  for (const r of group) {
    const star = r.priority ? "⭐" : "";
    const name = r.hasFolder
      ? `[${r.title}](roadmaps/${r.slug}/) · [↗](${roadmapUrl(r)})`
      : `[${r.title}](${roadmapUrl(r)})`;
    const prog = r.hasFolder ? `\`${bar(r.pct)}\` ${r.pct}% (${r.done}/${r.total})` : "—";
    out += `| ${star} | ${name} | ${EMOJI[r.status]} ${r.status} | ${prog} | ${r.touched} |\n`;
  }
  out += `\n`;
}

writeFileSync(join(ROOT, "DASHBOARD.md"), out.replace(/\n+$/, "\n"));

// index.md = the Quartz site home page (same table, titled for the site).
const home = `---\ntitle: tech-studies\n---\n${out.replace(/^# Dashboard\n/, "")}`;
writeFileSync(join(ROOT, "index.md"), home.replace(/\n+$/, "\n"));

console.log(`DASHBOARD.md + index.md written — ${rows.length} roadmaps, ${started} started, ${done} done.`);
